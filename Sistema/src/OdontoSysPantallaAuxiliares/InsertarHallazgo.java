    private void jButtonBuscarHallazgoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarHallazgoActionPerformed
        // TODO add your handling code here:
      BuscarHallazgo fmrBuscador = new BuscarHallazgo(null, true);
      fmrBuscador.setVisible(rootPaneCheckingEnabled);
      elHallazgo = null;  
      elHallazgo = fmrBuscador.getReturnStatus();
      
      if(elHallazgo!=null){
          labelHallazgoNombre.setText(elHallazgo.getNombre());
          labelHallazgoSimbolo.setText(elHallazgo.getSimbolol());          
      }
      
      
    }//GEN-LAST:event_jButtonBuscarHallazgoActionPerformed

    private void jButtonBuscarDienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarDienteActionPerformed
        // TODO add your handling code here:
        BuscarDiente BuscarDiente = new BuscarDiente(null, true);
        BuscarDiente.setVisible(rootPaneCheckingEnabled);
        elDiente = null;
        elDiente = BuscarDiente.getReturnStatus();
        
        if(elDiente != null){
            labelDiente.setText(elDiente.getNombre());
            labelDienteNom.setText(String.valueOf(elDiente.getNomenclatura()));
        }
    }//GEN-LAST:event_jButtonBuscarDienteActionPerformed
    
