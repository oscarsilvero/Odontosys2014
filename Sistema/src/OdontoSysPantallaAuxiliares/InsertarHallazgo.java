    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        if(elDiente != null && elHallazgo != null){         //elOdontograma != null && 
            elDetalleHallazgo = new DetalleHallazgo();
            String Especificacion = jTextFieldEspecificacion.getText();
            String Ubicacion = jTextFieldUbicacion.getText();            
            
            elDetalleHallazgo.setOdontograma(null);
            elDetalleHallazgo.setDiente(elDiente);
            elDetalleHallazgo.setHallazgo(elHallazgo);
            elDetalleHallazgo.setEspecificacion(Especificacion);
            elDetalleHallazgo.setUbicacion(Ubicacion);
            doClose(elDetalleHallazgo);
            
        }else if(elDiente != null && elHallazgo != null){
            
            elDetalleHallazgo = new DetalleHallazgo();
            String aux = jTextFieldEspecificacion.getText();
            String Especificacion = null;
            if(aux.length()>=1 && aux.length()<=4){
                Especificacion = jTextFieldEspecificacion.getText();
            }
            
            String Ubicacion = jTextFieldUbicacion.getText();          
            
            elDetalleHallazgo.setDiente(elDiente);
            elDetalleHallazgo.setHallazgo(elHallazgo);
            elDetalleHallazgo.setEspecificacion(Especificacion);
            elDetalleHallazgo.setUbicacion(Ubicacion);
            doClose(elDetalleHallazgo);
            
        }else{
        
            
            doClose(elDetalleHallazgo);
        }   
           
    }//GEN-LAST:event_okButtonActionPerformed

