    private void insertarEmpresa() {
        Empresa e = nuevaEmpresa();        
        boolean v = EmpresaVista.validarEmpresa(e);
        
        if(v){            //registro validado correctamente
            int i = EmpresaControlador.insertarEmpresa(e);
            if(i>0){
                JOptionPane.showMessageDialog(null,"Se insertó correctamente", "Insertar Empresa", WIDTH );
                limpiar();
                BotonInvisibles();
            }else{
                JOptionPane.showMessageDialog(null,"No se pudo insertar", "Error", WIDTH );
            }
        }
    }

    private void escribirEmpresa(Empresa empresa) {
        if(empresa!=null){    
            jTextFieldDNombres.setText(empresa.getNombre());
            jTextFieldRUC.setText(String.valueOf(empresa.getRuc()));
            jTextFieldDTel.setText(empresa.getTelefono());
            jTextFieldDDireccion.setText(empresa.getDireccion());
            jTextFieldDEmail.setText(empresa.getEmail());
            jTextFieldNombreContacto.setText(empresa.getNombreContacto());
            jTextFieldTeléfonoContacto.setText(empresa.getTelContacto());
            jTextFieldObservaciones.setText(empresa.getObservaciones());
            
            obtenerConvenios();
            obtenerEstadoCuenta();
            obtenerPendientes();
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "Empresa No encontrada", "aviso", WIDTH);
            jButtonModificar.doClick();
        }   
    }

