    private void inicializarTablas() {
        tablaConvenios.addColumn("Paciente");
        tablaConvenios.addColumn("Nro de cédula");
        tablaConvenios.addColumn("Teléfono");
        tablaConvenios.addColumn("Celular");
        
        tablaEstado.addColumn("Fecha");
        tablaEstado.addColumn("Descripción");
        tablaEstado.addColumn("Debe");
        tablaEstado.addColumn("Haber");    
        
        tablaPendientes.addColumn("Fecha");
        tablaPendientes.addColumn("Factura Nro");
        tablaPendientes.addColumn("Cliente");
        tablaPendientes.addColumn("Monto");
    }

    private void obtenerConvenios() {
        listaPac = new ArrayList();
        listaPac = ConvenioControlador.BuscarPacientesConConvenios(empresaActual);
        if(listaPac != null){
            for(Paciente p : listaPac){
                Object[] f = new Object[4];
                f[0] = p.getNombres() + " " + p.getApellidos();
                f[1] = formateador.format(p.getNroCi());
                f[2] = p.getTelLb();
                f[3] = p.getTelCel();
                
                tablaConvenios.addRow(f);
            }
        }
        
    }

    private void obtenerEstadoCuenta() {
        listaEst = new ArrayList();
        listaEst = EmpresaControlador.ObtenerEstadoCuenta(empresaActual);
        int saldo = 0;
        if(listaEst != null){
            for(MovimientoEmpresa m : listaEst){
                Object[] f = new Object[4];
                f[0] = fecha.format(m.getFecha());
                f[1] = m.getMovimiento();
                f[2] = formateador.format(m.getDebe());
                f[3] = formateador.format(m.getHaber());
                saldo = saldo + m.getDebe() - m.getHaber();
                
                tablaEstado.addRow(f);                
            }
        }
        if(saldo > 0){
            jTextFieldSaldo.setText(formateador.format(saldo));
            jButtonRealizarPago.setVisible(true);
        }else{
            jTextFieldSaldo.setText("0");
        }
    }

    private void obtenerPendientes() {
        listaFacConv = new ArrayList();
        listaFacConv = EmpresaControlador.FacturaPendiente(empresaActual.getIdempresa());
        int total = 0;
        if(listaFacConv != null){
            for(FacturaConvenio fc : listaFacConv){
                Object[] f = new Object[4];
                f[0] = fecha.format(fc.getFactura().getFecha());
                f[1] = fc.getFactura().getNroFactura();
                f[2] = fc.getFactura().getPaciente().getNombres() + " " +  fc.getFactura().getPaciente().getApellidos();
                f[3] = formateador.format(fc.getMonto());
                total = total + fc.getMonto();
                
                tablaPendientes.addRow(f);
                jTextFieldPendiente.setText(formateador.format(total));
            }
        }
        if(total > 0){
            jButtonFacturar.setVisible(true);
        }
    }
